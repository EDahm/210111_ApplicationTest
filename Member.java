package Prac;

public class Member {
	String name;
	String birth;
	String phnum;
	String addr;
	String email;
	char disabled;
	String memberNum;

	public Member(String name, String birth, String phnum, String addr, String email, char disabled, String memberNum) {
		this.name = name;
		this.birth = birth;
		this.phnum = phnum;
		this.addr = addr;
		this.email = email;
		this.disabled = disabled;
		this.memberNum = memberNum;
	}
}
